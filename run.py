def encrypt_data(data):
    cipher = AES.new(AES_KEY, AES.MODE_CBC, AES_IV)
    return cipher.encrypt(pad(data, AES.block_size))

# ================= MAIN LOGIC =================
def get_jwt(access_token):
    try:
        url = "https://loginbp.ggblueshark.com/MajorLogin"
        headers = {"User-Agent":"Dalvik/2.1.0","Content-Type":"application/x-www-form-urlencoded"}
        
        major = MajorLogin()
        major.game_name = "free fire"
        major.open_id = "test"
        major.access_token = access_token
        
        data = encrypt_data(major.SerializeToString())
        res = requests.post(url, data=data, headers=headers, verify=False, timeout=10)
        
        if res.status_code == 200:
            m = MajorLoginRes()
            m.ParseFromString(res.content)
            return m.token
    except Exception as e:
        print("Error:", e)
    return None

